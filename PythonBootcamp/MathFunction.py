import math

print("ceil(4.4) = ", math.ceil(4.4))
print("floor(4.4) = ", math.floor(4.4))
print("factorial(4) = ", math.factorial(4))
print("fmod(5, 4) = ", math.fmod(5, 4))  # returns remainder of division
print("trunc(4.4) = ", math.trunc(4.9))  # receive a float and return an int
print("pow(2, 2) = ", math.pow(2, 2))  # return x^y
print("sqrt(4) = ", math.sqrt(4))  # return square root
print("math.pi = ", math.pi)
print("math.e = ", math.e)
